const { withDangerousMod } = require('expo/config-plugins');
const fs = require('fs');
const path = require('path');

/**
 * Fix macOS /var vs /private/var symlink mismatch during EAS local builds.
 *
 * Problem: Xcode's resolveAppEntry returns an absolute path using /var/folders/...
 * but Metro resolves its project root to /private/var/folders/... (via fs.realpathSync).
 * This causes Metro to generate an incorrect relative path for the entry file.
 *
 * Solution: Set ENTRY_FILE to a relative path in .xcode.env.local so the Xcode
 * build script skips absolute path resolution entirely.
 */
module.exports = function fixEntryFile(config) {
  return withDangerousMod(config, [
    'ios',
    async (config) => {
      const iosPath = config.modRequest.platformProjectRoot;
      const envLocalPath = path.join(iosPath, '.xcode.env.local');

      const content = [
        '# Auto-generated by fix-entry-file plugin',
        '# Fixes /var vs /private/var symlink mismatch on macOS EAS local builds',
        'export ENTRY_FILE="node_modules/expo-router/entry.js"',
        '',
      ].join('\n');

      fs.writeFileSync(envLocalPath, content);

      return config;
    },
  ]);
};
